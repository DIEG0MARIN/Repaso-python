edad = 60
estatura = 1.6
print('La edad es:', edad)
print('La estatura es: ', estatura)
# 1 manera de uso de formato:
print('La edad es:', edad, 'La estatura es: ', estatura)
# 2da manera de uso de formato:
print(f"La edad es : {edad} y la estatura es: {estatura}")
print(f"La edad mas la estatura es: {edad + estatura}")
# 3ra manera de uso de formato:
print('La edad es: {} y la altura es: {}'.format(edad, estatura))


